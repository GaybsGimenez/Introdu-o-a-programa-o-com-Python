"""
Para pesquisar se uma string está dentro de outra e obter o índice da primeira ocorrência, use o método find().

"""

s = "Alô mundo"
s.find("mun")
s.find("ok")

# O método find() retorna -1 se a string não for encontrada. e o índice da primeira ocorrência se for encontrada.

"""
Se o objetivo for pesquisar a última ocorrência de uma string, use o método rfind().

"""
s = "Um dia de sol"
s.rfind("d")
# 7, pois a última ocorrência de d está no índice 7 da string s, contando a partir do 0

 
